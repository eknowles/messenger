const fb = require('./../../lib/facebook');
const logger = require('./../../lib/logger');
const User = require('../../model/user/user-facade');

/*
 * Message Event
 *
 * This event is called when a message is sent to your page. The 'message'
 * object format can vary depending on the kind of message that was received.
 * Read more at https://developers.facebook.com/docs/messenger-platform/webhook-reference/message-received
 *
 * For this example, we're going to echo any text that we get. If we get some
 * special keywords ('button', 'generic', 'receipt'), then we'll send back
 * examples of those bubbles to illustrate the special message bubbles we've
 * created. If we receive a message with an attachment (image, video, audio),
 * then we'll simply confirm that we've received the attachment.
 *
 */
function receivedMessage(event) {

  var senderID = event.sender.id;
  var recipientID = event.recipient.id;
  var timeOfMessage = event.timestamp;
  var message = event.message;

  logger.info(`Received message for user ${senderID} and page ${recipientID} at ${timeOfMessage} with message: ${message.text}`);

  var isEcho = message.is_echo;
  var messageId = message.mid;
  var appId = message.app_id;
  var metadata = message.metadata;

  // You may get a text or attachment but not both
  var messageText = message.text;
  var messageAttachments = message.attachments;
  var quickReply = message.quick_reply;

  if (isEcho) {
    // Just logging message echoes to console
    let md = metadata ? ` with metadata ${metadata}` : ``;
    logger.info(`Received echo for message ${messageId} and app ${appId}${md}`);
    return;
  } else if (quickReply) {
    var quickReplyPayload = quickReply.payload;
    logger.info(`Quick reply for message ${messageId} with payload ${quickReplyPayload}`);
    fb.sendTextMessage(senderID, {text: `Quick reply tapped`});
    return;
  }

  var user = {};

  fb.setTyping(senderID, true);
  User.findOne({facebookId: senderID})
    .then(usr => usr ? usr : User.createBasic(senderID))
    .then(u => user = u);

  if (user.firstName) {
    fb.sendTextMessage(senderID, {text: `Hey ${user.firstName}, how are you doing?`});
  }

  return;

  if (messageText) {
    // If we receive a text message, check to see if it matches any special
    // keywords and send back the corresponding example. Otherwise, just echo
    // the text we received.
    switch (messageText) {
      case 'image':
        sendImageMessage(senderID);
        break;

      case 'gif':
        sendGifMessage(senderID);
        break;

      case 'audio':
        sendAudioMessage(senderID);
        break;

      case 'video':
        sendVideoMessage(senderID);
        break;

      case 'file':
        sendFileMessage(senderID);
        break;

      case 'button':
        sendButtonMessage(senderID);
        break;

      case 'generic':
        sendGenericMessage(senderID);
        break;

      case 'receipt':
        sendReceiptMessage(senderID);
        break;

      case 'quick reply':
        sendQuickReply(senderID);
        break;

      case 'read receipt':
        sendReadReceipt(senderID);
        break;

      case 'typing on':
        sendTypingOn(senderID);
        break;

      case 'typing off':
        sendTypingOff(senderID);
        break;

      case 'account linking':
        sendAccountLinking(senderID);
        break;

      default:
        sendTextMessage(senderID, messageText);
    }
  } else if (messageAttachments) {
    sendTextMessage(senderID, "Message with attachment received");
  }
}

module.exports = receivedMessage;
